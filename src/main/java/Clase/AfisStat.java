package Clase;

public class AfisStat {
    private int id;
    private String nume;
    private String desc;
    private int count;

    public AfisStat() {
    }

    public int getId() {
        return id;
    }

    public String getNume() {
        return nume;
    }


    public int getCount() {
        return count;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
